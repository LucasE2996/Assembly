2D4A
A 100
MOV AH, 02 ; COLOCA 02h EM AH, PARA IMPRIMIR CARACTER NA TELA
MOV DL, BH ; CARREGA DL COM O BYTE MAIS SIGNIFICATIVO DE BX
MOV CL, 04 ; COLOCA 04h EM CL, PARA DESLOCAR 4 BITS
SHR DL, CL ; DESLOCA DL A QUANTIDADE DE BITS EM CL
CALL 0124  ; CHAMA ROTINA DE IMPRESSAO
MOV DL, BH ; CARREGA DL COM O BYTE MAIS SIGNIFICATIVO DE BX
AND DL, 0F ; FAZ AND PARA CARREGAR SOMENTE O NIBBLE MENOR
CALL 0124  ; CHAMA ROTINA DE IMPRESSAO
MOV DL, BL ; CARREGA DL COM O BYTE MENOS SIGNIFICATIVO DE BX
SHR DL, CL ; DESLOCA DL A QUANTIDADE DE BITS EM CL
CALL 0124  ; CHAMA ROTINA DE IMPRESSAO
MOV DL, BL ; CARREGA DL COM O BYTE MENOS SIGNIFICATIVO DE BX
AND DL, 0F ; FAZ AND PARA CARREGAR SOMENTE O NIBBLE MENOR
CALL 0124  ; CHAMA ROTINA DE IMPRESSAO
INT 20     ; FIM DO PROGRAMA
ADD DL, 30 ; SUB - SOMA 30h A DL, PARA CONVERTER PARA CARACTERE
CMP DL, 39 ; COMPARA COM VALOR LIMITE 39h
JLE 012F   ; SE FOR DIFERENTE, PULA PROXIMA LINHA
ADD DL, 07 ; SE FOR IGUAL, SOMA MAIS 7 PARA CONVERTER PARA CARACTERE
INT 21     ; IMPRIME O CARACTERE EM DL
RET        ; RETORNA PARA O PROGRAMA PRINCIPAL